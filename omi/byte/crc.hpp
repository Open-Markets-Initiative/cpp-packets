// calculate 16 bit crc check
constexpr std::uint16_t crc16(std::uint16_t seed, void *buffer, std::uint32_t count) {
    auto current = static_cast<const uint8_t*>(buffer);

    auto crc = seed;

    while (count--) {
        crc = (crc << 8) ^ crc16table[((crc >> 8) ^ *current++)];
    }

    return crc;
}

