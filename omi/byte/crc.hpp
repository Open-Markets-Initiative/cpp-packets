// calculate 32 bit crc check
constexpr std::uint32_t crc(std::uint32_t crc, const void *buffer, std::uint64_t count) {
    auto current = static_cast<const uint8_t*>(buffer);

    crc = crc ^ ~0U;

    while (count--) {
        crc = crc32table[(crc ^ *current++) & 0xFF] ^ (crc >> 8);
    }

    return crc ^ ~0U;
}

